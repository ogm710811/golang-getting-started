package main

func main() {
	add := 1 + 2
	println(add)

	substract := 10 - 5
	println(substract)

	remainder := 5 % 2
	println(remainder)

	increment := 1
	increment++
	println(increment)
	increment++
	println(increment)

	// others operators
	increment += 5
	println(increment)

	increment *= 10
	println(increment)

}
